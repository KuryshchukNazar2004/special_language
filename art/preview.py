def preview_art(ascii_art):
    """
    Виводить попередній перегляд ASCII-арту.

    Параметри:
        ascii_art (str): Текст, що містить ASCII-арт, який потрібно показати.

    Опис:
        Ця функція приймає строку з ASCII-артом і виводить її в консоль. Якщо передано порожнє значення або
        None, буде виведено повідомлення про помилку, що попередній перегляд неможливий.

    Приклад:
        preview_art(generate_ascii_art("Hello", "slant", "red", 50, 10, "#"))

    Використання:
        Використовувати для виведення попереднього перегляду ASCII-арту перед фінальним збереженням або обробкою.

    Викидає:
        Немає, але перевіряє, чи є ASCII-арт перед його виведенням.
    """
    if ascii_art:
        print("\nПопередній перегляд:")
        print(ascii_art)
    else:
        print("ASCII-арт не вдалося створити, тому попередній перегляд неможливий.")
