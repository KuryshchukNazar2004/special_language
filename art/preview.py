def preview_art(ascii_art):
    """
    Виводить попередній перегляд згенерованого ASCII-арту на екран.

    Параметри:
    - ascii_art (str): Строка, що містить згенерований ASCII-арт.

    Якщо ASCII-арт створено успішно, функція виводить його на екран.
    Якщо ASCII-арт не було створено або він дорівнює None, виводиться повідомлення про помилку.
    """
    if ascii_art:
        print("\nПопередній перегляд:")
        print(ascii_art)  # Виводимо ASCII-арт на екран
    else:
        print("ASCII-арт не вдалося створити, тому попередній перегляд неможливий.")
