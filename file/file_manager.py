import os

def save_to_file(ascii_art):
    """
    Зберігає ASCII-арт у текстовий файл.

    Параметри:
        ascii_art (str): Текст, що містить ASCII-арт, який необхідно зберегти.

    Опис:
        Функція приймає ASCII-арт, видаляє кольорові коди (якщо вони є), і зберігає результат у текстовий файл. 
        Якщо ASCII-арт не було створено (None), функція виведе відповідне повідомлення. Також, якщо папка для збереження 
        файлів не існує, вона буде створена автоматично.

    Процес:
        - Якщо ASCII-арт містить кольорові коди (наприклад, для червоного, зеленого чи синього тексту), вони будуть видалені.
        - Файл з ASCII-артом зберігається в папці `txt_files`. Якщо цієї папки немає, вона буде створена.
        - Користувач вводить назву файлу (з розширенням `.txt`), і ASCII-арт зберігається у цей файл.

    Приклад:
        save_to_file(generate_ascii_art("Hello", "slant", "red", 50, 10, "#"))

    Використання:
        Використовувати для збереження створеного ASCII-арту в текстовий файл для подальшого використання.

    Викидає:
        Викидає помилку, якщо збереження файлу не вдається (наприклад, через неправильні права доступу до папки).
    """
    if ascii_art is None:
        print("Неможливо зберегти ASCII-арт, оскільки його не вдалося створити.")
        return

    plain_ascii_art = ascii_art.replace("\033[31m", "").replace("\033[32m", "").replace("\033[34m", "").replace("\033[0m", "")

    if not os.path.exists('txt_files'):
        os.makedirs('txt_files')

    file_name = input("Введіть назву файлу для збереження (з розширенням .txt): ")
    file_path = os.path.join('txt_files', file_name)  # Шлях до файлу

    try:
        with open(file_path, "w", encoding="utf-8") as file:
            file.write(plain_ascii_art)
        print(f"ASCII-арт збережено у файл: {file_path}")
    except Exception as e:
        print(f"Помилка при збереженні файлу: {e}")
